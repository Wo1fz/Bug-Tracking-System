    private void SubmitBugButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitBugButtonActionPerformed
        // TODO add your handling code here:
         //get text from all fields from report bug page.
        String [] array = new String[jTable1.getColumnCount()];  //declare array
        array[0] = ReportBugIDField.getText();          // id
        array[1] = ReportBugTitleField.getText();       // title
        array[2] = ReportBugKeywordField.getText();     // keyword
        array[3] = ReportDateField.getText();           // date
        array[4] = ReportBugDescField.getText();        // description
        array[5] = ReportBugSummaryField.getText();     // summary
        array[6] = name+" - "+id;                       // reporter name
        array[7] = "null";                                // assignee
        array[8] = "null";                                // assigned developer
        array[9] = "not resolved";                      // current status
        array[10] = "null";                               // assigned severity
        array[11] = "null";                               // assigned priority
        array[12] = "null";                               //patch submitted
        array[13] = "null";                              // test results
        array[14] = "null";                              // Tester
        
        ReportBugController RBC = new ReportBugController();
        
        if(RBC.ValidateFields(array))
        {
            BugReport bug = new BugReport();
            bug.setBugData(array);
            boolean success = bug.SaveReportedBug(array);
            if(success)
            {
                //update in main table
                DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
                model.addRow(array);
                DisplayHomePage();
                setDataDetailsPage(array);
                //set report bug page fields to empty values;
                ReportBugTitleField.setText("");       // title
                ReportBugKeywordField.setText("");     // keyword
                ReportBugDescField.setText("");        // description
                ReportBugSummaryField.setText("");     //summary
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Fields cannot be empty!","Report Unsuccessful", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_SubmitBugButtonActionPerformed

