    private void BugsWithPatchesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BugsWithPatchesActionPerformed
        // TODO add your handling code here:
        jTabbedPane1.setSelectedIndex(0);
        //clear selection from main table
        jTable1.getSelectionModel().clearSelection();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        try
	{	
	    FileReader in = new FileReader("BugReport.txt");
            BufferedReader br = new BufferedReader(in);
            // Read files
            String readLine = "";
            while ((readLine = br.readLine()) != null) 
            {
                String [] row = readLine.split(":");
                if(row[9].equals("Pending"))
                    model.addRow(row);
            }	
            br.close();
            in.close();
	} 
	catch (IOException ex) {
            Logger.getLogger(DeveloperUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BugsWithPatchesActionPerformed

