    private void ReportBugButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReportBugButtonActionPerformed
        // TODO add your handling code here:
        //clear selection from main table
        jTable1.getSelectionModel().clearSelection();
        String pattern = "dd/MM/yyyy";
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat(pattern);
        String date = simpleDateFormat.format(new Date());
        String id = "";
        ReportDateField.setText(date);
        
        //generate bug id
        try
	{	
            File myObj = new File("BugReport.txt");
            Scanner myReader = new Scanner(myObj);
			
            // Read files
            while (myReader.hasNextLine()) 
            {
		String readLine = myReader.nextLine();
		String [] splitLine = readLine.split(":");
                id = splitLine[0];
            }	
            myReader.close();
	} 
		
	// Catch error
	catch (FileNotFoundException e) 
	{
            System.out.println("An error occurred.");
            e.printStackTrace();
        }
        if(id.equals(""))
        {
            int number = 10000;
            id = Integer.toString(number);
        }
        else
        {
            int number = Integer.parseInt(id);
            number++;
            id = Integer.toString(number);
        }
        ReportBugIDField.setText(id);
        DisplayReportPage();
    }//GEN-LAST:event_ReportBugButtonActionPerformed

